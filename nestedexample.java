public class nestedexample {
    public static void main(String[] args) {
        int marks=95;
        if(marks>=90){
            System.out.println("grade:A+");
        }
        else if(marks>=75){
            System.out.println("grade:A");

        }
        else if(marks>=50){
            System.out.println("grade:B");
        }
        else{
            System.out.println("you failed in the exam");
        }
    }
    
}
